package com.bean;

public class UserBean {

	private int userId;
	private String firstName;
	private String lastName;
	private String email;
	private String mobileNo;
	private String placesToVisit;
	private String budget;
	private String yourLocation;
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getFirstName() {
		return firstName;
	}
	
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getMobileNo() {
		return mobileNo;
	}
	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}
	public String getPlacesToVisit() {
		return placesToVisit;
	}
	public void setPlacesToVisit(String placesToVisit) {
		this.placesToVisit = placesToVisit;
	}
	public String getBudget() {
		return budget;
	}
	public void setBudget(String budget) {
		this.budget = budget;
	}
	public String getYourLocation() {
		return yourLocation;
	}
	public void setYourLocation(String yourLocation) {
		this.yourLocation = yourLocation;
	}
	
	
}
